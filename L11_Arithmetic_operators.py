# 算术运算符
# 1 加减乘除 + - * /
num1 = 6
num2 = 1.5
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
# 2 整除 //
# 除法取整，向下取整
print(99 // 10, -99 // -10, 99 // -10, -99 // 10)
# 3 取余 %
# 余数 = 被除数 - 除数 * 商
print(99 % 10, -99 % -10, 99 % -10, -99 % 10)
# 4 幂运算符 **
num1 = 10
num2 = 4
print(num1 ** num2)  # 10^4
